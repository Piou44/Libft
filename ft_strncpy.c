#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (n && *src)
	{
		*dst++ = *src++;
		--n;
	}
	while (n-- > 0)
		*dst++ = '\0';
	return (dst);
}
